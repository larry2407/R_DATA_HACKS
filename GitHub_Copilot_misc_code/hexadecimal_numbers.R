
# hexadecimal numbers in R ------------------------------------------------
# Convert the following hexadecimal numbers to RGB colour codes:
macaron <- c("#FFB7AE", "#E18B76", "#8D533E")
tuile <- c("#FF9575", "#CE614A", "#AD4847")
tuile2 <- c("#FF8860", "#F3533B", "#BD2822") # chrome color picker

# Convert hexadecimal numbers to RGB colour codes
col2rgb(macaron)
col2rgb(tuile)
col2rgb(tuile2)

